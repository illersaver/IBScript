    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Making sure that all fields are filled
        if (jNameField.getText().equals("") ||
            jLastNameField.getText().equals("") || 
            jLoginField.getText().equals("") ||
            jPasswordField.getText().equals("")) {
            
            jRegisterMessage.setText("Field is empty");
            return;
        }
        
        // Making sure that login is available
        for (User user : QuizManager.users) {
            if (jLoginField.getText().equals(user.getLogin())) {
                jRegisterMessage.setText("Login is already taken");
                return;
            }
        }
        
        // Making sure that passwords are identical
        if (!jPasswordField.getText().equals(jPasswordAgainField.getText())) {
            jRegisterMessage.setText("Password is different");
            return;
        }
        
        // Creating user and saving it to database
        User newUser = new User(jNameField.getText().replaceAll("\\s", "") + " " + jLastNameField.getText().replaceAll("\\s", ""),
                jLoginField.getText().replaceAll("\\s", ""),
                jPasswordField.getText().replaceAll("\\s", ""));
        
        newUser.save();
        DatabaseAccess.writeToFile("database/users.txt", newUser.toDatabase());
        
        jRegisterMessage.setText("Account created");
    }//GEN-LAST:event_jButton1ActionPerformed

