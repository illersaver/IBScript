    // Next question button
    private void jNextQuizButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jNextQuizButtonActionPerformed
        QuizManager.currentQuestionID++;
        
        if (QuizManager.currentQuestion == null) {
            jQuestion.setVisible(false);
            return;
        }
        
        if (QuizManager.currentQuestion.isCorrect(jQuestionAnswerField.getText().toLowerCase())) {
            QuizManager.points++;
        }
        
        jQuestionAnswerField.setText("");
        
        startQuestion();
    }//GEN-LAST:event_jNextQuizButtonActionPerformed

