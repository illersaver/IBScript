    // Login button
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        for (User user : QuizManager.users) {
            if (user.enter(jLoginFieldLogin.getText(), jPasswordFieldLogin.getText())) {
                QuizManager.currentUser = user;
                
                jWelcomeLabel.setText("welcome " + QuizManager.currentUser.getName() + "!");
                
                jQuizSelector.setVisible(true);
                jQuizSelector.setBounds(0, 0, 386, 300);
                
                // getting all quizes
                File[] dir = new File("database/questions").listFiles();

                // we add a button for each quiz, so you could start them
                for (int i = 0; i < dir.length; i++)
                {
                    String name = dir[i].getName();
                    
                    JButton button = new JButton(name.replaceAll(".txt", ""));

                    button.setBounds(5, 25 + (25 * (i + 1)), 100, 25);
                    button.addActionListener(new ActionListener() {
                        @Override
                        public void actionPerformed(ActionEvent e)
                        {
                            jQuestion.setVisible(true);
                            jQuestion.setBounds(0, 0, 386, 300);
                            
                            // initialising question/quiz
                            QuizManager.currentQuestionID = 0;
                            QuizManager.points = 0;
                            
                            QuizManager.currentQuestionPath = name;
                            jNextQuizButton.setText("Next");
                            jQuestionText.setText("You've got " + QuizManager.points + " points!");
                            jQuizName.setText(name.replaceAll(".txt", ""));
                            
                            jTipArea.setVisible(true);
                            jQuestionAnswerField.setVisible(true);
                            jQuestionHelp.setEnabled(true);
                            jLogoutButton.setEnabled(false);
                            
                            startQuestion();
                        }
                    });

                    jPanel5.add(button);
                    button.setVisible(true);
                }
                
                return;
            }
        }
        
        jLoginMessage.setText("Login or password is incorrect");
    }//GEN-LAST:event_jButton3ActionPerformed

