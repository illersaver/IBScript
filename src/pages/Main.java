    // Start question helper
    public void startQuestion()
    {
        jQuizName.setText(QuizManager.currentQuestionPath.replace(".txt", ""));
        
        QuizManager.currentQuestion = DatabaseAccess.getQuestion("database/questions/" + QuizManager.currentQuestionPath,
                QuizManager.currentQuestionID);
        
        if (QuizManager.currentQuestion == null) {
            jNextQuizButton.setText("Exit");
            jQuestionText.setText("You've got " + QuizManager.points + " points!");
            jQuizName.setText("End");
            jTipArea.setVisible(false);
            jQuestionHelp.setEnabled(false);
            jQuestionAnswerField.setVisible(false);
            jLogoutButton.setEnabled(true);
            
            DatabaseAccess.writeToFile("database/results.txt",
                    QuizManager.currentUser.getName() + "/" +
                    QuizManager.currentQuestionPath.replace(".txt", "") + "/" +
                    QuizManager.points + " out of " +
                    QuizManager.currentQuestionID + "\n");
            return;
        }
        
        jQuestionText.setText(QuizManager.currentQuestion.getText());
        
        String tips = "";
        
        for (String tip : QuizManager.currentQuestion.GetTips()) {
            tips += tip + "\n";
        }
        jTipArea.setText(tips);
    }
    
